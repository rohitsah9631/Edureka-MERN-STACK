// math object in js. helps us to perform some mathematical operation in javascript.

// How  to round a decimal value. (4.6)
console.log(Math.round(4.6));//5
//Math.round returns nearest integer
console.log(Math.round(4.3));//4
console.log(Math.round(4.5));//5

//How to get nearest integer rounded up?
console.log(Math.ceil(4.3));//5

//How to get nearest integer round down?
console.log(Math.floor(4.8));//4

// Get square root of a number.
console.log(Math.sqrt(100));//10

//max value from a collection of value?
console.log(Math.max(20,25,30,65,70,68,95));//95

//min value from a collection of value?
console.log(Math.min(20,25,30,65,70,68,95));//20
